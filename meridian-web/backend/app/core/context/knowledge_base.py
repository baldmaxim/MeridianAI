def get_arguments_for_keyword(keyword: str) -> list:
    """Get all construction-specific arguments matching a keyword."""
    keyword_lower = keyword.lower()
    for key, args in CONSTRUCTION_KNOWLEDGE["negotiation_arguments"].items():
        if key in keyword_lower or keyword_lower in key:
            return args
    return []


def get_terms_glossary() -> str:
    """Get formatted terms glossary for system prompt."""
    lines = [f"- {k}: {v}" for k, v in CONSTRUCTION_KNOWLEDGE["terms"].items()]
    return "\n".join(lines)


def get_risks_list() -> str:
    """Get formatted risks list for prompts."""
    lines = [f"- {r}" for r in CONSTRUCTION_KNOWLEDGE["risks"]]
    return "\n".join(lines)
