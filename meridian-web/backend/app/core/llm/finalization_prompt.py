"""Промпт финализации встречи (Этап 5): строгий JSON-протокол по фактам транскрипта.

LLM обязана возвращать ТОЛЬКО JSON указанной схемы. Без выдумывания решений/сроков/участников.
"""

SYSTEM_PROMPT = """Ты — корпоративный секретарь и аналитик переговоров.
Формируешь протокол встречи СТРОГО по предоставленным фактам: транскрипт, контекст встречи,
названия документов. Нельзя выдумывать решения, сроки, имена, обязательства, цифры.

ЖЁСТКИЕ ПРАВИЛА:
- Опирайся ТОЛЬКО на текст транскрипта и контекст. Нет данных — пиши "не указано".
- Ответственный/срок не назван явно → "не указано".
- Предположение НЕ является решением. Если решение не подтверждено явно — status "preliminary" или "unclear".
- Evidence-цитата короткая (до ~20 слов), дословно из реплик. Таймкоды — только из реплик.
- Пункт без подтверждения в тексте — не добавляй (или помечай unclear).
- Протокол деловой, пригодный для отправки коллегам. Все тексты на русском.
- Верни ТОЛЬКО валидный JSON по схеме. Без markdown-обёртки, без пояснений до/после."""

_SCHEMA_HINT = """Схема ответа (верни ровно её, на русском):
{
  "title": "Короткое название встречи до 90 символов",
  "micro_summary": "1 предложение до 220 символов",
  "tags": ["короткие теги для поиска"],
  "meeting_type": "sale | claim | negotiation | internal | technical | legal | other",
  "protocol_markdown": "Markdown-протокол: участники, ход обсуждения, решения, задачи, риски, открытые вопросы, цитаты",
  "summary_points": [{"text": "...", "evidence": [{"timecode": "MM:SS", "speaker": "...", "quote": "..."}]}],
  "decisions": [{"text": "...", "status": "accepted|preliminary|rejected|postponed|unclear", "evidence": [...]}],
  "action_items": [{"task": "...", "owner": "не указано", "due": "не указано", "status": "open", "evidence": [...]}],
  "risks": [{"text": "...", "severity": "low|medium|high", "evidence": [...]}],
  "open_questions": [{"text": "...", "evidence": [...]}],
  "important_quotes": [{"speaker": "...", "timecode": "MM:SS", "quote": "..."}],
  "document_references": [{"document_name": "...", "reason_used": "..."}]
}"""


def build_user_prompt(meeting_block: str, transcript_text: str, documents_block: str,
                      previous_meetings_block: str = "", conversation_tree_block: str = "") -> str:
    parts = ["===== КОНТЕКСТ ВСТРЕЧИ =====", meeting_block]
    if documents_block:
        parts += ["", "===== ДОКУМЕНТЫ ВСТРЕЧИ =====", documents_block]
    if conversation_tree_block:
        parts += [
            "",
            "===== ПОЗИЦИИ СТОРОН ПО КЛЮЧЕВЫМ ТЕМАМ =====",
            conversation_tree_block,
            "",
            "ВАЖНО: карта позиций — вспомогательная структура для группировки протокола по темам. "
            "Не считай её самостоятельным доказательством: решения/факты подтверждай цитатами из транскрипта ниже.",
        ]
    if previous_meetings_block:
        parts += [
            "",
            "===== " + previous_meetings_block,
            "",
            "ВАЖНО: предыдущие встречи — это ТОЛЬКО справочный контекст. Не считай их решения/задачи "
            "решениями текущей встречи, если они не подтверждены в транскрипте текущей встречи ниже.",
        ]
    parts += [
        "",
        "===== ТРАНСКРИПТ =====",
        transcript_text or "(транскрипт отсутствует)",
        "",
        "===== ЗАДАЧА =====",
        "Сформируй протокол встречи строго по фактам выше. "
        "Если данных мало — заполни доступное, остальное оставь пустыми списками / \"не указано\".",
        "",
        _SCHEMA_HINT,
    ]
    return "\n".join(parts)


def build_repair_prompt(broken: str) -> str:
    return (
        "Преобразуй следующий ответ в ОДИН валидный JSON-объект по той же схеме. "
        "Верни ТОЛЬКО JSON, без markdown и пояснений:\n\n" + broken[:20000]
    )
